#include<stdio.h>
int main(){
	int num1=10;
	int num2=20;
	printf("%d+%d=%d\n", num1, num2, num1+num2);
	printf("%d*%d=%d\n", num1, num2, num1*num2);
	printf("%d-%d=%d\n", num1, num2, num1-num2);
	printf("%d/%d=%d\n", num1, num2, num1/num2);
	printf("%d%%d=%d\n", num1, num2, num1%num2);
return 0;
}
